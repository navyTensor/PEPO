function [CR] = RightMoveOdd(ix, CR, A, B)
%%  contract odd column: 1, 3, 5
%   A[1][0] = A2_5              % A[1][0]: 31-32-36-51-50
%   A[1][1] = A4                % A[1][1]: 30-29-28
%   A[1][2] = A2                % A[1][2]: 35
%   A[1][3] = A2                % A[1][3]: 27
%   A[1][4] = A4                % A[1][4]: 26-25-24
%   A[1][5] = A2                % A[1][5]: 34
%   A[1][6] = A2                % A[1][6]: 23
%   A[1][7] = A4                % A[1][7]: 22-21-20
%   A[1][8] = A2                % A[1][8]: 33

Da = size(A{ix,1},2);
Db = size(B{ix,1},2);
D2 = Da*Db;
C5 = reshape(ncon({conj(B{ix,1}), A{ix,1}}, {[1,-1,-3], [1,-2,-4]}, [1]),[D2, D2]);
CR = reshape(CR, [D2, D2*D2*D2]);
CR = C5*CR;

CR = reshape(CR, [Db, Da, Db, Da, D2, D2]);
CR = ncon({CR, conj(B{ix,2}), A{ix,2}, conj(B{ix,3}), A{ix,3}}, ...
     {[1,2,3,4,-5,-6], [8,-1,-3,1,6], [8,-2,-4,2,7], [5,6,3], [5,7,4]}, [3,4,5,1,6,2,7,8]);

CR = reshape(CR, [D2, Db, Da, Db, Da, D2]);
CR = ncon({CR, conj(B{ix,4}), A{ix,4}, conj(B{ix,6}), A{ix,6}}, ...
     {[-1,1,2,4,5,-6], [3,-2,1], [3,-3,2], [6,-4,4], [6,-5,5]}, [1,2,3,4,5,6]);
CR = ncon({CR, conj(B{ix,5}), A{ix,5}}, {[-1,1,3,2,4,-6], [5,-2,-4,1,2], [5,-3,-5,3,4]}, [1,2,3,4,5]);

CR = reshape(CR, [D2, D2, Db, Da, Db, Da]);
CR = ncon({CR, conj(B{ix,7}), A{ix,7}, conj(B{ix,9}), A{ix,9}}, ...
     {[-1,-2,1,2,4,5], [3,-3,1], [3,-4,2], [6,-5,4], [6,-6,5]}, [1,2,3,4,5,6]);

CR = ncon({CR, conj(B{ix,8}), A{ix,8}}, {[-1,-2,1,3,2,4], [5,-3,-5,1,2], [5,-4,-6,3,4]}, [1,2,3,4,5]);

end
