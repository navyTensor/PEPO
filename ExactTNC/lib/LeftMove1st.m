function [CL] = LeftMove1st(A, B)
%%  contract first column
%   A[0][0] = A6                % A[0][0]: 13-12-17-11-10-9
%   A[0][1] = A3                % A[0][1]: 8
%   A[0][2] = A2                % A[0][2]: 16
%   A[0][3] = A2_3              % A[0][3]: 7-6-5
%   A[0][4] = A3                % A[0][4]: 4
%   A[0][5] = A2                % A[0][5]: 15
%   A[0][6] = A2_7              % A[0][6]: 3-2-1-0-14-18-19

Da = size(A{1,1},2);
Db = size(B{1,1},2);
D2 = Da*Db;
C6 = reshape(ncon({conj(B{1,1}), A{1,1}}, {[1,-1,-3], [1,-2,-4]}, [1]),[D2,D2]);
T8 = reshape(ncon({conj(B{1,2}), A{1,2}}, {[1,-1,-3,-5], [1,-2,-4,-6]}, [1]),[D2,D2,D2]);    % T8(d,l,r)
T16 = reshape(ncon({conj(B{1,3}), A{1,3}},{[1,-1,-3], [1,-2,-4]}, [1]),[D2,D2]);

Cu = reshape(ncon({C6, T8, T16}, {[1,-1], [-3,1,2], [2,-2]}, [1,2]),[D2*D2, D2]);

C3 = reshape(ncon({conj(B{1,4}), A{1,4}}, {[1,-1,-3], [1,-2,-4]}, [1]),[D2,D2]);
T4 = reshape(ncon({conj(B{1,5}), A{1,5}}, {[1,-1,-3,-5], [1,-2,-4,-6]}, [1]),[D2,D2,D2]);    % T4(d,l,r)
T15 = reshape(ncon({conj(B{1,6}), A{1,6}}, {[1,-1,-3], [1,-2,-4]}, [1]),[D2,D2]);
C7 = reshape(ncon({conj(B{1,7}), A{1,7}}, {[1,-1,-3], [1,-2,-4]}, [1]),[D2,D2]);

Cd = reshape(ncon({C3, T4, T15, C7}, {[-1,2], [1,2,3], [3,-2], [1,-3]}, [1,2,3]),[D2, D2*D2]);

CL = Cu * Cd;       % CL(1'1,2'2,3'3,4'4)

end
